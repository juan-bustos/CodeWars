using System;
using System.Collections.Generic;
using System.Text;

namespace CodeWars
{
    public class SumOfMultiples
    {
        public static int findSum (int n)
        {
            int multiplesOfFive = n;
            return multiplesOfFive;
        }
        public static int multipleOfFive (int x)
        {
            int multipleOfFive = 0;
            while (multipleOfFive != x)
            {
                multipleOfFive += 5;
            }
            return multipleOfFive;
        }
    }
}
